public class Lesson11_Arsya {
    

    public static void main(String[] args) {
        
        int i = 100;
        while(i <= 10){
            System.out.println(i);



            i++;
        }



        int hitungan = 100;
        do{
            System.out.println(hitungan);
            hitungan ++;
        }
        while(hitungan <= 10);


    }

}
